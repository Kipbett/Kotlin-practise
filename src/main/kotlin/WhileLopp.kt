fun main(){
    var i = 0

    //While Loop
    println("============ While Loop ================")
    while(i <=10){
        println("i: $i")
        i += 1
    }

    //Do-While Loop
    i = 0
    println("=============== Do-While Loop =============")
    do {
        println("i: $i")
        i += 1
    } while(i <= 10)
}